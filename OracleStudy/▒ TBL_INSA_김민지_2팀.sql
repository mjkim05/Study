--          HR계정의 EMPLOYEES 테이블에서 커미션 받는 사람의 수와
--          안받는 사람의 수를 조회한다.
--          출력형태 ---------------
--              구분        인원수
--          ------------------------
--          커미션받는사원    XXX
--          커미션없는사원    XXX
--          모든사원          XXX
SELECT NVL(T.커미션,'모든사원') , COUNT(*)
FROM 
(
    SELECT CASE WHEN COMMISSION_PCT  IS NULL THEN '커미션없는사원'
                WHEN COMMISSION_PCT  IS NOT NULL THEN '커미션받는사원'
                ELSE '오류' 
                END "커미션"
    FROM EMPLOYEES
)T
GROUP BY ROLLUP(T.커미션);
