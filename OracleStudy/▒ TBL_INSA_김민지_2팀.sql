--33. TBL_INSA 테이블에서 BASICPAY + SUDANG 이 
--    100만원 미만, 100만원 이상~200만원 미만, 
--    200만원 이상인 직원들의 수 조회.    
SELECT T.급여 , COUNT(*)
FROM
(
    SELECT CASE WHEN (BASICPAY + SUDANG) < 1000000  THEN '100만원미만'
                WHEN (BASICPAY + SUDANG)  BETWEEN 1000000 AND 2000000 THEN '100만원~200만원'
                WHEN (BASICPAY + SUDANG)  >= 2000000 THEN '200만원이상'
                ELSE '그이상'
                END "급여"
    FROM TBL_INSA
) T
GROUP BY T.급여;
