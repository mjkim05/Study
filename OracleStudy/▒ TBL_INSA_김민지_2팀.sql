SELECT BUSEO "부서명" , TRUNC(AVG(BASICPAY+SUDANG)) "평균급여"
       ,CASE WHEN TRUNC(AVG(BASICPAY+SUDANG)) > 2000000 THEN 'A등급' 
            WHEN TRUNC(AVG(BASICPAY+SUDANG)) > 1500000 THEN 'B등급' 
            WHEN TRUNC(AVG(BASICPAY+SUDANG)) < 1500000 THEN 'C등급' 
            ELSE '등급을 알 수 없습니다.' 
       END "등급"
FROM TBL_INSA
GROUP BY BUSEO;
